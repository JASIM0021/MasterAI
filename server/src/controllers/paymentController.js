  /**
   * Migrate user to global credit system
   */
  async migrateToGlobalCredits(req, res) {
    try {
      const userId = req.user._id;
      const credits = await Credit.findOrCreateForUser(userId);

      if (credits.isGlobalCreditsEnabled()) {
        return res.json({
          success: true,
          message: 'User already using global credit system',
          credits: credits.getGlobalCreditInfo()
        });
      }

      await credits.migrateToGlobalCredits();

      res.json({
        success: true,
        message: 'Successfully migrated to global credit system',
        credits: credits.getGlobalCreditInfo()
      });
    } catch (error) {
      console.error('Migrate to global credits error:', error);
      res.status(500).json({
        success: false,
        message: 'Failed to migrate to global credit system'
      });
    }
  }

