  /**
   * Get payment analytics and monitoring data
   */
  async getPaymentAnalytics(req, res) {
    try {
      const { timeframe = '7d' } = req.query;

      // Calculate date range
      const endDate = new Date();
      const startDate = new Date();

      switch (timeframe) {
        case '24h':
          startDate.setHours(startDate.getHours() - 24);
          break;
        case '7d':
          startDate.setDate(startDate.getDate() - 7);
          break;
        case '30d':
          startDate.setDate(startDate.getDate() - 30);
          break;
        case '90d':
          startDate.setDate(startDate.getDate() - 90);
          break;
        default:
          startDate.setDate(startDate.getDate() - 7);
      }

      // Aggregate payment data
      const [totalStats, statusBreakdown, packageStats, dailyTrends] = await Promise.all([
        // Total statistics
        PaymentOrder.aggregate([
          {
            $match: {
              createdAt: { $gte: startDate, $lte: endDate }
            }
          },
          {
            $group: {
              _id: null,
              totalOrders: { $sum: 1 },
              totalRevenue: { $sum: { $cond: [{ $eq: ['$status', 'success'] }, '$amount', 0] } },
              totalCreditsAwarded: { $sum: { $cond: [{ $eq: ['$creditsAwarded', true] }, { $add: ['$creditsToAward', '$bonusCredits'] }, 0] } },
              averageOrderValue: { $avg: { $cond: [{ $eq: ['$status', 'success'] }, '$amount', null] } }
            }
          }
        ]),

        // Status breakdown
        PaymentOrder.aggregate([
          {
            $match: {
              createdAt: { $gte: startDate, $lte: endDate }
            }
          },
          {
            $group: {
              _id: '$status',
              count: { $sum: 1 },
              revenue: { $sum: { $cond: [{ $eq: ['$status', 'success'] }, '$amount', 0] } }
            }
          }
        ]),

        // Package performance
        PaymentOrder.aggregate([
          {
            $match: {
              createdAt: { $gte: startDate, $lte: endDate }
            }
          },
          {
            $lookup: {
              from: 'creditpackages',
              localField: 'packageId',
              foreignField: '_id',
              as: 'package'
            }
          },
          {
            $unwind: '$package'
          },
          {
            $group: {
              _id: '$packageId',
              packageName: { $first: '$package.name' },
              totalOrders: { $sum: 1 },
              successfulOrders: { $sum: { $cond: [{ $eq: ['$status', 'success'] }, 1, 0] } },
              totalRevenue: { $sum: { $cond: [{ $eq: ['$status', 'success'] }, '$amount', 0] } },
              creditsAwarded: { $sum: { $cond: [{ $eq: ['$creditsAwarded', true] }, { $add: ['$creditsToAward', '$bonusCredits'] }, 0] } }
            }
          },
          {
            $sort: { totalRevenue: -1 }
          }
        ]),

        // Daily trends
        PaymentOrder.aggregate([
          {
            $match: {
              createdAt: { $gte: startDate, $lte: endDate }
            }
          },
          {
            $group: {
              _id: {
                year: { $year: '$createdAt' },
                month: { $month: '$createdAt' },
                day: { $dayOfMonth: '$createdAt' }
              },
              date: {
                $first: {
                  $dateToString: {
                    format: '%Y-%m-%d',
                    date: '$createdAt'
                  }
                }
              },
              totalOrders: { $sum: 1 },
              successfulOrders: { $sum: { $cond: [{ $eq: ['$status', 'success'] }, 1, 0] } },
              failedOrders: { $sum: { $cond: [{ $eq: ['$status', 'failed'] }, 1, 0] } },
              cancelledOrders: { $sum: { $cond: [{ $eq: ['$status', 'cancelled'] }, 1, 0] } },
              revenue: { $sum: { $cond: [{ $eq: ['$status', 'success'] }, '$amount', 0] } }
            }
          },
          {
            $sort: { '_id.year': 1, '_id.month': 1, '_id.day': 1 }
          }
        ])
      ]);

      // Calculate success rate
      const statusMap = {};
      statusBreakdown.forEach(item => {
        statusMap[item._id] = item.count;
      });

      const totalOrders = Object.values(statusMap).reduce((sum, count) => sum + count, 0);
      const successRate = totalOrders > 0 ? ((statusMap.success || 0) / totalOrders * 100).toFixed(2) : 0;

      // Format response
      const analytics = {
        timeframe,
        dateRange: {
          start: startDate.toISOString(),
          end: endDate.toISOString()
        },
        summary: {
          totalOrders: totalStats[0]?.totalOrders || 0,
          totalRevenue: totalStats[0]?.totalRevenue || 0,
          totalCreditsAwarded: totalStats[0]?.totalCreditsAwarded || 0,
          averageOrderValue: Math.round(totalStats[0]?.averageOrderValue || 0),
          successRate: parseFloat(successRate)
        },
        statusBreakdown: statusBreakdown.map(item => ({
          status: item._id,
          count: item.count,
          revenue: item.revenue,
          percentage: totalOrders > 0 ? ((item.count / totalOrders) * 100).toFixed(2) : 0
        })),
        packagePerformance: packageStats.map(item => ({
          packageId: item._id,
          packageName: item.packageName,
          totalOrders: item.totalOrders,
          successfulOrders: item.successfulOrders,
          totalRevenue: item.totalRevenue,
          creditsAwarded: item.creditsAwarded,
          conversionRate: item.totalOrders > 0 ? ((item.successfulOrders / item.totalOrders) * 100).toFixed(2) : 0
        })),
        dailyTrends: dailyTrends.map(item => ({
          date: item.date,
          totalOrders: item.totalOrders,
          successfulOrders: item.successfulOrders,
          failedOrders: item.failedOrders,
          cancelledOrders: item.cancelledOrders,
          revenue: item.revenue,
          successRate: item.totalOrders > 0 ? ((item.successfulOrders / item.totalOrders) * 100).toFixed(2) : 0
        }))
      };

      res.json({
        success: true,
        analytics
      });
    } catch (error) {
      console.error('Payment analytics error:', error);
      res.status(500).json({
        success: false,
        message: 'Failed to fetch payment analytics'
      });
    }
  }

